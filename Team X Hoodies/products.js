

// Get the modal
var modal = document.getElementById("myModal");

// Get the button that opens the modal
var btn = document.getElementsByClassName("product-link-1");

// Get the <span> element that closes the modal
var span = document.getElementsByClassName("close")[0];

// When the user clicks the button, open the modal
for (var i = 0; i < btn.length; i++) {
  btn[i].onclick = function() {
    modal.style.display = "block";
    document.querySelector(".product-image img").src = this.querySelector("img").src;
    document.getElementById("product-name").innerHTML = this.querySelector(".overlay h1").innerHTML;
    document.getElementById("product-price").innerHTML = this.querySelector(".overlay p").innerHTML;
    document.getElementById("product-description").innerHTML = "This hoodie features a graphic print of an astronaut sitting on a swing that is being held up by the moon, creating an eye-catching design that is sure to turn heads. Made from high-quality materials, this hoodie is soft, warm and durable, making it ideal for everyday wear. The black and white color scheme gives it a modern, minimalist look that will complement any outfit.";
  }
}

